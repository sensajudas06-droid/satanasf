17. Törenlerim, kahinim tarafından ayrıca açıklanacaktır; çok karışık 
olanları... Araştırılıp, bulunacaktır değişik törenlerim ya da 
vahyedilecektir O'na. Ama bu kitabımda,  sayısal olarak az olan 
toplumlarım için, çok kolay ve pratik bir tören şekli de tarif ediyorum. 
Onda da tadilat yapabilirsiniz, zamana ve zemine ve şartlara göre. 
 
18. Eski törenlerim aynen geçerlidir;  çünkü bana atfedilen her şey, 
