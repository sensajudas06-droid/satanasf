şeyler de vardır.  Ama mecburi ve değişmez değildir bunlar da. 
 
15. Benim şerefime ve benim rızamı almak için ve benim kutsamam için 
ve benden kudret almak için yüzyıllardır yapılmış ve yapılmakta olan 
Törenler veya Ayinler veya Ritüeller vardır; hangi deyimi kabul ederseniz. 
Bu törenlerin çoğu benim tarafımdan verilmemiştir insanlara; ancak 
