V 
 
1. Bana yapılan hizmetler, sizin sevaplarınızdır benim nezdimde. 
Yapabileceğiniz hizmetler sayısızdır aklınızla onayladıkça; ama önemli 
hizmetlerimi size bildiriyorum ki, her zaman rahmetim üzerinizde olsun.  
 
2.  Bence makbul olan bir hizmetiniz, dışarıda olan birini uyandırarak, 
bana getirmektir. Bunu yapmak isteyen müridim, dikkatli davranmalı son 
derece. Önce ürkütmemek için beni bilmeyeni ve sonra kendisini 
koymamak için ortaya ihtiyatsızca. Müridim her aşamada adım adım 
ilerleyerek, dışardakinin şüphe ve merakını uyarmalı. Ama bunda sıkı bir 
dikkat ve ihtiyat gereklidir. Elbette bu,  sahte dinlerin baskısı altında olan 
yobaz yerler ve ülkeler için geçerlidir. Açıkça davranabilirse seçkinim 
veya çoksa o yerde seçkinlerim, bazı müridlerim koyabilirler ortaya 
kendilerini ve inançlarını. 
 
3. İleri bir hizmetiniz de,  seçkinimin ailesi dışarıda olanlardansa, 
seçkinimin, ailesinden birini veya birkaçını veya hepsini benim yoluma 
getirmesidir.  
 
4. Bir hizmetiniz de bence makbul olan, müridimin eşi inançsızsa ya da 
sahte tanrının sahte dinine inanıyorsa, onu çekmesidir toplumuma. 
Uymasını sağlamaktır zevk veren kurallarıma. 
 
5. En iyisi de, bir müridimin, eşi çok inançlıysa kötü tanrıya, onu baştan 
çıkartmasıdır tatlılıkla.  
 
6. Tatlılıkla yaklaşın eşlerinize ve ikna edin onu;  uyararak şüphelerini 
kötülük tanrısına. Cezbedin onu cinsel özgürlükle ve teşvik edin. Ama 
saygınızı da eksik etmeyin ondan. Her gayretinize rağmen eşiniz 
dönmezse yoluma, onu terkedin ve birlikte olun yolumda olanla; ama 
bunu da yapamaz veya yapmak istemezseniz, siz de reddedin beni ona 
33 
karşı ve aldatın onu her açıdan. Bu da, bana hizmetinizdir ve sevabınızdır 
sizin açınızdan. 
 
7. Sonraki önemli hizmetlerinizden biri de, müridimin, bana bağlanmadan 
önce hangi sahte dinde görünmüşse, onun kurallarını ve yasaklarını 
çiğnemesidir teker teker. Her gün, en az birini ufak veya büyük. Kadın 
seçkinlerimin tahrik etmeleri dışarıdaki erkekleri ve onların kafalarını 
karıştırmaları özel din günlerinde. 
 
8. Kendi toplumunda bilinmeyen müridimin, sanki kendisi bilmez gibi 
davranarak çevreye yönelttiği iman sarsacak soru ve düşünceleri de bana 
hizmettir aslında. Yargılamasına sebep olun insanların, dini ve ahlakı ve 
kurallar ve şeriatı ve tanrıyı.   
 
9. Eşinizle ya da başkasıyla olan cinselliğinizi de bana adamanız, hizmettir 
bana. Herkes benim seçkinimse, açıkça ve eşiniz dışarıdaysa, içinizden. 
Ve kalıplaşmış kuralların, mantıksız ahlak kurallarına karşı davranmanız 
da, hizmettir bana. 
 
10. Tersine çevirmek ibadetini ve şehvete boğmak ve küçük düşürmek 
Arabın dininin ve Kilisenin dininin ve Yahudinin dininin tapınma ve 
kutsama törenlerini;  bu da sevabınızdır ayrıca.  
 
11. Bir önemli hizmetiniz de, benim öğretimin bu kitabını yaymasıdır 
müridimin, insanlar arasında kendisini tehlikeye atmadan. Zamanına ve 
yerine göre. Kullanın her tür iletişim aracınızı, gücünüz yettiğince. 
 
12. Bunları yaparak, karşı çıkmış  olursunuz içinizdeki baskıya ve tabuya 
ve daha kolay kırarsınız zincirlerinizi ve yaşarsınız maddenin ve tenin 
zevkini ve yenersiniz kendinizi;  ama bunların, bilmediğiniz daha başka 
ödülleri de vardır sizin için.  
 
13. Benim için hizmetleriniz, sayısızdır. Bunlardan başka, aklınızla 
bulabilirsiniz her hizmeti. Ama hizmetinizle ibadetiniz farklı şeylerdir. 
Tapınımım törenseldir; hizmetim ise günlük ve serbest. 
 
14. Tapınımımda ve ibadetimde de çok şey beklemem sizden; yeterki, 
aklınızla benimle bir olun (Bütünleşin). Bütün günler ve zamanlar ve 
