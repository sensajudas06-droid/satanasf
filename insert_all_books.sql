doğmayı, buraya ya da daha iyisine. 
 
22. Bendedir, yeniden doğmanın bilimi ve gücü. İstediğimi, istediğim 
şekilde yeniden gönderirim dünyaya. Bunlardan bazıları özel görevlerle 
