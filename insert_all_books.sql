29. Nasıl olur da bir tanrı, bütün insanların yaratıcısı ve sahibiyse ve onları 
gözetip koruyansa ve kendisinden başka yaratan yoksa, nasıl   olur da 
emreder, kendi dininden olmayanı ve kabul etmeyeni öldürmeyi ya da 
işkence etmeyi? Nasıl olur da bütün duygu ve düşüncenin kaynağı olan 
tanrı, günah yazar kendi verdiği istekler ve düşünceler için? Uyanın artık! 
Düşünün bunları ve isyan edin, sizi ruhsal olarak sömürene.  
 
30. Onun bir tasarrufu yoktur üzerinizde. Aynı şekilde yalandır, ödülü de 
cezası da. Ondan daha fazla tanrıcıdır, bu hurafelerle kendilerini yücelten 
din adamları. Zevk alarak, Allah rızası için adam öldürenleri ve kutsal 
cihada sarılanları. Haçlı seferlerini din adına yaparak, kasalarını 
dolduranları; işkenceden cinsel zevk duyanları ve insanları kısıtlayarak, 
kendisinin cennete gideceğine inananları. 
 
31. Ben savaşın tanrısı değilim; ama savaşçı bir tanrıyım da gerektiğinde. 
Cezasını bulacak, insanlarımı kendi cehenneminde yaşatanlar; ama 
demiyorum ki, bu, ahirette olacak. Yaşarken ödeyecekler yaptıklarını. 
Müridlerimin çoğalması ve reddetmesi onları, bu bile eritecektir 
içlerindeki buzları. 
 
32. Bana!  Bana gelin. Kurtuluşunuz bendedir sadece!  Beni tanıyıp, 
tapmasanız da yolumdan yürüyün ve aklınızı çalıştırın sadece. Akla vurun 
bu kitabımı, şartlanmışlıklardan ve geleneklerden arınmış bir zihinle. 
