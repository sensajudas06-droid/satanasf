bahçesinde olmaktı. Başka dünyalarda da vardır Tanrıların bahçeleri. Ama hepsi 
içiçedir başka bir boyutta ve hepsi aynı yerdedir aslında milyonlarca ışık yılı 
uzakta da olsalar. 
 
